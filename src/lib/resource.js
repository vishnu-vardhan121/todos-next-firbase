export const LineData = (order) => {
  const sortedUsers = [...users];
  if (order == "ascending") {
    return {
      labels: sortedUsers
        .sort((a, b) => a.age - b.age)
        .map((user) => user.username),
      datasets: [
        {
          label: "ages",
          data: sortedUsers
            .sort((a, b) => a.age - b.age)
            .map((user) => user.age),
          fill: false,
        },
      ],
    };
  } else if (order == "descending") {
    return {
      labels: sortedUsers
        .sort((a, b) => b.age - a.age)
        .map((user) => user.username),
      datasets: [
        {
          label: "ages",
          data: sortedUsers
            .sort((a, b) => b.age - a.age)
            .map((user) => user.age),
          fill: false,
        },
      ],
    };
  }
  return {
    labels: users.map((user) => user.username),
    datasets: [
      {
        label: "ages",
        data: users.map((user) => user.age),
        fill: false,
      },
    ],
  };
};
